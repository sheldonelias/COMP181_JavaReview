public class _5_Switch
{
    public _5_Switch()
    {
        char ch = 'b';

        switch (ch)
        {
            case 'a':
                System.out.println("Case was a");
                break;
            case 'b':
                System.out.println("Case was b");
                break;
            case 'c':
                System.out.println("Case was c");
                break;
            default:

        }


    }
}
